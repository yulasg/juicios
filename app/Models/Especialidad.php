<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\SoftDeletes;

class Especialidad extends Model
{
    use HasFactory;
    use SoftDeletes;
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    protected $table = "especialidades";
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    protected $fillable = ['internacional', 'descripcion'];
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //Relacion uno a muchos
    public function juicios()
    {
        return $this->hasMany('App\Models\Juicio');
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //Relacion uno a muchos
    public function configuraciones()
    {
        return $this->hasMany('App\Models\Configuracion');
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public function personas()
    {
        return $this->hasManyThrough('App\Models\Persona', 'App\Models\Configuracion');
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public function actores()
    {
        return $this->hasManyThrough('App\Models\Actor', 'App\Models\Configuracion');
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public function getInternacionalAttribute($value)
    {
        switch ($this->attributes['internacional']) {
            case 'N':
                $value =  'Nacional';
                break;
            case 'I':
                $value = 'Internacional';
                break;
        }
        return ($value);
    }
}
