    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public function getFechaAttribute($value)
    {
        return $value ? Carbon::parse($value)->format('d/m/Y h:i:s A') : null;
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    protected $appends =
    [
        'fecha_movimiento'
    ];
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public function getFechaMovimientoAttribute()
    {
        $value = $this->attributes['fecha'];
